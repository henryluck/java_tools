    private void jButton_getTaskListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_getTaskListActionPerformed
        try {
            String url = jTextField_Url.getText().trim();
//            if (url == null || !url.toLowerCase().contains("baidu.com")) {
            if (url == null || url.equals("")) {
                JOptionPane.showMessageDialog(this, "地址没有输入，或者不是一个有效的百度音乐列表地址", "错误信息", JOptionPane.WARNING_MESSAGE);
                return;
            }
            DownTask.getInstance().getTaskList(url);
            jTable_TaskList.updateUI();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "获取错误:" + ex.getMessage(), "错误信息", JOptionPane.WARNING_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton_getTaskListActionPerformed

