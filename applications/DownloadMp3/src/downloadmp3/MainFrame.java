    /**
     * 以之前获得的下载任务列表为输入参数，开始下载
     */
    private void jButton_StartDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_StartDownActionPerformed
        try {
            jButton_StartDown.setEnabled(false);
            jBDir.setEnabled(false);
            DownTask.getInstance().downloadAllMp3();
            jProgressBar_DownLoad.setMinimum(0);
            jProgressBar_DownLoad.setMaximum(DownTask.getInstance().allTaskCount);
            jProgressBar_DownLoad.setStringPainted(true);
            final ScheduledThreadPoolExecutor scheduler = (ScheduledThreadPoolExecutor) Executors.newScheduledThreadPool(1);
            Runnable runnable =
                    new Runnable() {

                        public void run() {
                            if (DownTask.getInstance().getExecutorService().isTerminated()) {
                                jButton_StartDown.setEnabled(true);
                                jBDir.setEnabled(true);
                                scheduler.shutdown();
                            }
                            try {
                                jTable_Download.updateUI();
                                jProgressBar_DownLoad.setValue(DownTask.getInstance().finishTaskCount);
                                Thread.sleep(1000);
                            } catch (Exception e) {
                            	e.printStackTrace();
                            }
                        }
                    };

            scheduler.scheduleAtFixedRate(runnable, 5, 1, TimeUnit.SECONDS);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "获取错误:" + ex.getMessage(), "错误信息", JOptionPane.WARNING_MESSAGE);
            ex.printStackTrace();
        }
}//GEN-LAST:event_jButton_StartDownActionPerformed

